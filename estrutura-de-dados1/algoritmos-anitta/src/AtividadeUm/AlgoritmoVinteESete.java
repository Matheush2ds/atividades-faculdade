package AtividadeUm;

public class AlgoritmoVinteESete {
    public static void main(String[] args) {
        int x = 2;
        int y = 5;

        System.out.println("x = " + x);
        x = x+1;
        System.out.println("y = " + y);
        y = y-1;

        System.out.println("Novo valor de x: " +x);
        System.out.println("Novo valor de y: " +y);

    }
}
